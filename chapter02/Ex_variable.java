package chapter02;

public class Ex_variable {
    public static void main(String[] args){

        System.out.println("6+3");
        System.out.println(6+3);

        //변수 선언 초기화 및 읽어오기
        int x = 4, y = 2;
        System.out.println(x+y); //덧셈
        System.out.println(x-y); //뺄셈
        System.out.println(x*y); //곱셈
        System.out.println(x/y); //나눗셈

    }
}
